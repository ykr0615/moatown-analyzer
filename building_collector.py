def fetch_buildings(sigungu_cd, bjdong_cd, num_rows=1000, page=1):
    """건축물대장 API 호출 (curl 사용)"""
    url = (
        f"https://apis.data.go.kr/1613000/BldRgstHubService/getBrTitleInfo"
        f"?serviceKey={API_KEY}"
        f"&sigunguCd={sigungu_cd}&bjdongCd={bjdong_cd}"
        f"&platGbCd=0&numOfRows={num_rows}&pageNo={page}"
    )
    try:
        result = subprocess.run(
            ["curl", "-s", url],
            capture_output=True, text=True, timeout=30,
        )
        if result.returncode != 0:
            return [], 0

        root = ET.fromstring(result.stdout)
        total = int(root.findtext(".//totalCount", "0"))
        items = root.findall(".//item")

        buildings = []
        for item in items:
            use_apr = item.findtext("useAprDay", "")
            year = use_apr[:4] if len(use_apr) >= 4 else ""

            buildings.append({
                "건물명": item.findtext("bldNm", ""),
                "주용도": item.findtext("mainPurpsCdNm", ""),
                "사용승인일": use_apr,
                "사용승인연도": year,
                "대지면적": float(item.findtext("platArea", "0") or "0"),
                "연면적": float(item.findtext("totArea", "0") or "0"),
                "용적률": float(item.findtext("vlRat", "0") or "0"),
                "건폐율": float(item.findtext("bcRat", "0") or "0"),
                "구조": item.findtext("strctCdNm", ""),
                "지번": item.findtext("platPlc", ""),
                "등록구분": item.findtext("regstrGbCdNm", ""),
            })

        return buildings, total
    except Exception as e:
        print(f"    API 오류: {e}")
        return [], 0


def analyze_dong(sigungu_cd, bjdong_cd, gu_name, dong_name):
    """특정 동의 노후도 분석"""
    buildings, total = fetch_buildings(sigungu_cd, bjdong_cd)

    if not buildings:
        return None

    df = pd.DataFrame(buildings)

    # 주거용 건물 필터 (단독주택, 공동주택, 다세대, 다가구, 연립)
    residential_keywords = ["단독주택", "공동주택", "다세대", "다가구", "연립"]
    residential = df[df["주용도"].str.contains("|".join(residential_keywords), na=False)].copy()

    if residential.empty:
        return None

    residential["사용승인연도"] = pd.to_numeric(residential["사용승인연도"], errors="coerce")
    residential = residential.dropna(subset=["사용승인연도"])
    residential["건물나이"] = CURRENT_YEAR - residential["사용승인연도"]

    total_res = len(residential)
    old_30 = len(residential[residential["건물나이"] >= 30])
    old_20 = len(residential[residential["건물나이"] >= 20])
    new_10 = len(residential[residential["건물나이"] < 10])

    return {
        "구": gu_name,
        "동": dong_name,
        "시군구코드": sigungu_cd,
        "법정동코드": bjdong_cd,
        "전체건물수": total,
        "주거용건물수": total_res,
        "30년이상": old_30,
        "20년이상": old_20,
        "10년미만_신축": new_10,
        "노후도_30년": round(old_30 / total_res * 100, 1) if total_res > 0 else 0,
        "노후도_20년": round(old_20 / total_res * 100, 1) if total_res > 0 else 0,
        "신축비율": round(new_10 / total_res * 100, 1) if total_res > 0 else 0,
        "평균건물나이": round(residential["건물나이"].mean(), 1),
        "평균용적률": round(residential["용적률"].mean(), 1),
    }


def collect_all_seoul(progress_callback=None):
    """서울 전역 동별 노후도 수집

    Args:
        progress_callback: 진행률 콜백 함수 (current, total, dong_name)
    """
    DATA_DIR.mkdir(exist_ok=True)
    results = []
    total = len(SEOUL_DONG_CODES)

    for i, (sigungu, bjdong, gu, dong) in enumerate(SEOUL_DONG_CODES):
        if progress_callback:
            progress_callback(i + 1, total, f"{gu} {dong}")
        else:
            print(f"[{i+1}/{total}] {gu} {dong}...")

        result = analyze_dong(sigungu, bjdong, gu, dong)
        if result:
            results.append(result)

        time.sleep(0.3)  # API rate limit 방지

    df = pd.DataFrame(results)

    # 저장
    output_path = DATA_DIR / "seoul_obsolescence.csv"
    df.to_csv(output_path, index=False, encoding="utf-8-sig")
    print(f"\n저장 완료: {output_path} ({len(df)}개 동)")

    return df


