def _geocode_address(address):
    """주소 → 좌표 변환 (카카오 API 우선, Nominatim 폴백)"""
    import requests as req
    kakao_key = os.environ.get("KAKAO_REST_API_KEY", "")

    if kakao_key:
        try:
            r = req.get(
                "https://dapi.kakao.com/v2/local/search/address.json",
                params={"query": address},
                headers={"Authorization": f"KakaoAK {kakao_key}"},
                timeout=5,
            )
            if r.status_code == 200:
                docs = r.json().get("documents", [])
                if docs:
                    return float(docs[0]["y"]), float(docs[0]["x"])
        except Exception:
            pass
    return None, None


def _haversine(lat1, lon1, lat2, lon2):
    """두 좌표 간 거리 (미터)"""
    from math import radians, sin, cos, sqrt, atan2
    R = 6371000
    dlat = radians(lat2 - lat1)
    dlon = radians(lon2 - lon1)
    a = sin(dlat / 2) ** 2 + cos(radians(lat1)) * cos(radians(lat2)) * sin(dlon / 2) ** 2
    return R * 2 * atan2(sqrt(a), sqrt(1 - a))


def _cluster_by_coords(items, max_distance=200):
    """좌표 기반 클러스터링 (반경 200m 이내 건물을 같은 블록으로)"""
    remaining = list(range(len(items)))
    clusters = []
    while remaining:
        seed = remaining.pop(0)
        cluster = [seed]
        slat, slon = items[seed]["lat"], items[seed]["lon"]
        i = 0
        while i < len(remaining):
            idx = remaining[i]
            if _haversine(slat, slon, items[idx]["lat"], items[idx]["lon"]) <= max_distance:
                cluster.append(idx)
                remaining.pop(i)
            else:
                i += 1
        if len(cluster) >= 3:
            clusters.append(cluster)
    return clusters


def _score_block(group, scorer, gu_name, dong_name,
                 residential_kw, religious_kw, commercial_kw):
    """블록 하나의 스코어 계산"""
    res = group[group["주용도"].str.contains("|".join(residential_kw), na=False)]
    res_with_year = res[res["건물나이"] > 0]
    total_res = len(res_with_year)
    if total_res < 3:
        return None

    old_30 = len(res_with_year[res_with_year["건물나이"] >= 30])
    new_10 = len(res_with_year[res_with_year["건물나이"] < 10])
    ratio = old_30 / total_res * 100
    new_ratio = new_10 / total_res * 100
    avg_age = res_with_year["건물나이"].mean()

    years_to_50 = None
    if ratio < 50:
        for future in range(1, 11):
            future_old = len(res_with_year[res_with_year["건물나이"] + future >= 30])
            if future_old / total_res * 100 >= 50:
                years_to_50 = future
                break

    churches = len(group[group["주용도"].str.contains("|".join(religious_kw), na=False)])
    large_comm = len(group[(group["주용도"].str.contains("|".join(commercial_kw), na=False)) & (group["대지면적"] > 500)])
    commercials = len(group[group["주용도"].str.contains("|".join(commercial_kw), na=False)])

    avg_vlrat = res_with_year["용적률"].mean() if not res_with_year.empty else 0
    block_score = scorer.score_area({
        "buildings": pd.DataFrame({"사용승인연도": res_with_year["사용승인연도"].astype(str).tolist()}),
        "avg_land_price": 3000000,
        "current_far": avg_vlrat if avg_vlrat > 0 else 150,
        "allowed_far": 250,
        "church_count": churches,
        "large_commercial": large_comm,
        "new_building_ratio": new_ratio / 100,
        "owner_occupied_ratio": 0.5,
        "block_area": 50000,
    })

    issues = []
    if ratio < 50:
        issues.append(f"노후도 미충족 ({years_to_50}년 후 충족 예상)" if years_to_50 else "노후도 미충족 (10년 내 충족 어려움)")
    if churches > 0:
        issues.append(f"교회 {churches}개")
    if large_comm > 0:
        issues.append(f"대형상가 {large_comm}개")
    if new_ratio > 20:
        issues.append(f"신축 {new_ratio:.0f}%")

    status = "충족" if ratio >= 50 else (f"{years_to_50}년 후 충족" if years_to_50 else "미충족")

    search_addr = ""
    for _, row in group.iterrows():
        jibun = str(row.get("지번", ""))
        if jibun and dong_name in jibun:
            search_addr = jibun.replace("번지", "").strip()
            break
    if not search_addr:
        buns = sorted(group["본번"].unique())
        search_addr = f"서울 {gu_name} {dong_name} {buns[len(buns)//2]}"

    buns = sorted(group["본번"].unique())
    min_bun, max_bun = min(buns), max(buns)
    label = f"{min_bun}~{max_bun}번지" if min_bun != max_bun else f"{min_bun}번지"

    return {
        "구간": label, "검색주소": search_addr,
        "주거건물": total_res, "전체건물": len(group),
        "노후도": round(ratio, 1), "평균나이": round(avg_age, 1),
        "신축": new_10, "교회": churches, "상업시설": commercials,
        "요건충족": ratio >= 50, "충족상태": status,
        "블록점수": block_score["종합_점수"], "등급": block_score["등급"],
        "문제점": ", ".join(issues) if issues else "-",
    }


def _analyze_blocks(df, gu_name="", dong_name="", use_coords=True):
    """블록 단위 분석 (use_coords=True: 좌표 기반, False: 본번 기반)"""
    if df.empty or "본번" not in df.columns:
        return []

    residential_kw = ["주택", "공동", "다세대", "다가구", "연립"]
    religious_kw = ["종교"]
    commercial_kw = ["근린", "판매", "업무", "숙박"]

    df_unique = df.drop_duplicates(subset=["본번", "부번"]).copy()
    kakao_key = os.environ.get("KAKAO_REST_API_KEY", "")
    scorer = MoatownScorer()

    # === 좌표 기반 클러스터링 (카카오 API) ===
    if kakao_key and use_coords:
        print(f"    좌표 기반 클러스터링 (카카오 API)...")
        geo_items = []
        addrs_done = set()
        for _, row in df_unique.iterrows():
            addr = str(row.get("지번", "")).replace("번지", "").strip()
            if not addr or addr in addrs_done:
                continue
            addrs_done.add(addr)
            lat, lon = _geocode_address(addr)
            if lat and lon:
                geo_items.append({"lat": lat, "lon": lon, **row.to_dict()})

        if len(geo_items) >= 5:
            clusters = _cluster_by_coords(geo_items, max_distance=200)
            blocks = []
            for cidx_list in clusters:
                group = pd.DataFrame([geo_items[i] for i in cidx_list])
                b = _score_block(group, scorer, gu_name, dong_name,
                                 residential_kw, religious_kw, commercial_kw)
                if b:
                    blocks.append(b)
            blocks.sort(key=lambda x: x["블록점수"], reverse=True)
            return blocks

    # === 본번 기반 클러스터링 (폴백) ===
    print(f"    본번 기반 클러스터링...")
    sorted_buns = sorted(df_unique["본번"].unique())
    if not sorted_buns:
        return []

    block_ranges = []
    current_block = [sorted_buns[0]]
    for i in range(1, len(sorted_buns)):
        if sorted_buns[i] - sorted_buns[i - 1] <= 10:
            current_block.append(sorted_buns[i])
        else:
            if len(current_block) >= 2:
                block_ranges.append(current_block)
            current_block = [sorted_buns[i]]
    if len(current_block) >= 2:
        block_ranges.append(current_block)

    blocks = []
    for bun_list in block_ranges:
        group = df_unique[df_unique["본번"].isin(bun_list)]
        b = _score_block(group, scorer, gu_name, dong_name,
                         residential_kw, religious_kw, commercial_kw)
        if b:
            blocks.append(b)
    blocks.sort(key=lambda x: x["블록점수"], reverse=True)
    return blocks


