def resolve_dong_name(dong_input):
    """행정동 이름을 법정동으로 변환

    1. 명시적 매핑 테이블에 있으면 사용
    2. 없으면 숫자를 제거해서 법정동 추측 (방이2동 → 방이동)
    3. 특수 케이스 처리 (가동 → 가 등)
    """
    dong_input = dong_input.strip()

    # 1. 명시적 매핑
    if dong_input in ADMIN_TO_LEGAL:
        return ADMIN_TO_LEGAL[dong_input]

    # 2. 숫자 제거로 자동 변환
    import re
    # "방이2동" → "방이동", "화곡6동" → "화곡동", "상계10동" → "상계동"
    auto = re.sub(r'\d+동$', '동', dong_input)
    if auto != dong_input and auto in DONG_CODE_MAP:
        return auto

    # "·" 포함 (종로1·2·3·4가동 등)
    auto2 = re.sub(r'[\d·]+가동$', '동', dong_input)
    if auto2 != dong_input and auto2 in DONG_CODE_MAP:
        return auto2

    # 그대로 반환
    return dong_input


def get_dong_code(dong_name):
    """동 이름으로 코드 조회

    1. DONG_CODE_MAP에서 찾기
    2. 없으면 API로 서울 전체 시군구를 스캔해서 찾기
    """
    legal = resolve_dong_name(dong_name)
    codes = DONG_CODE_MAP.get(legal)
    if codes:
        return codes, legal

    # DONG_CODE_MAP에 없는 경우 - API로 자동 탐색
    print(f"  '{legal}' 코드 자동 탐색 중...")
    sigungu_list = [
        "11110", "11140", "11170", "11200", "11215", "11230",
        "11260", "11290", "11305", "11320", "11350", "11380",
        "11410", "11440", "11470", "11500", "11530", "11545",
        "11560", "11590", "11620", "11650", "11680", "11710", "11740",
    ]

    for sigungu in sigungu_list:
        for bjdong_num in range(10100, 20000, 100):
            bjdong = str(bjdong_num)
            try:
                result = subprocess.run(['curl', '-s',
                    f'https://apis.data.go.kr/1613000/BldRgstHubService/getBrTitleInfo'
                    f'?serviceKey={API_KEY}&sigunguCd={sigungu}&bjdongCd={bjdong}&platGbCd=0&numOfRows=1&pageNo=1'
                ], capture_output=True, text=True, timeout=5)

                root = ET.fromstring(result.stdout)
                total = int(root.findtext('.//totalCount', '0'))
                if total > 0:
                    plat = root.findtext('.//platPlc', '')
                    if legal in plat:
                        print(f"  발견: {sigungu}/{bjdong} → {plat}")
                        # 캐시에 저장
                        DONG_CODE_MAP[legal] = (sigungu, bjdong)
                        return (sigungu, bjdong), legal
            except Exception:
                pass

    return None, legal


def fetch_all_buildings(sigungu_cd, bjdong_cd, max_pages=20):
    """건축물대장 전체 건물 조회 (페이지네이션, 최대 2000건)"""
    all_buildings = []

    for page in range(1, max_pages + 1):
        url = (
            f"https://apis.data.go.kr/1613000/BldRgstHubService/getBrTitleInfo"
            f"?serviceKey={API_KEY}"
            f"&sigunguCd={sigungu_cd}&bjdongCd={bjdong_cd}"
            f"&platGbCd=0&numOfRows=100&pageNo={page}"
        )
        try:
            result = subprocess.run(
                ["curl", "-s", url],
                capture_output=True, text=True, timeout=30,
            )
            root = ET.fromstring(result.stdout)
            total = int(root.findtext(".//totalCount", "0"))
            items = root.findall(".//item")

            for item in items:
                use_apr = item.findtext("useAprDay", "")
                year = int(use_apr[:4]) if use_apr and len(use_apr) >= 4 else 0

                bun_raw = item.findtext("bun", "0") or "0"
                ji_raw = item.findtext("ji", "0") or "0"
                bun_num = int(bun_raw.lstrip("0") or "0")

                all_buildings.append({
                    "건물명": item.findtext("bldNm", ""),
                    "주용도": item.findtext("mainPurpsCdNm", ""),
                    "사용승인연도": year,
                    "건물나이": CURRENT_YEAR - year if year > 0 else 0,
                    "대지면적": float(item.findtext("platArea", "0") or "0"),
                    "용적률": float(item.findtext("vlRat", "0") or "0"),
                    "지번": item.findtext("platPlc", ""),
                    "구조": item.findtext("strctCdNm", ""),
                    "본번": bun_num,
                    "부번": ji_raw.lstrip("0") or "0",
                })

            if len(all_buildings) >= total:
                break

        except Exception:
            break

    return all_buildings


