    old_20 = len(res_with_year[res_with_year["건물나이"] >= 20])
    new_10 = len(res_with_year[res_with_year["건물나이"] < 10])
    avg_age = res_with_year["건물나이"].mean()
    obsolescence_ratio = old_30 / total_res * 100
    new_ratio = new_10 / total_res * 100

